-- How to SELECT all table with columns
SELECT * FROM `table_name`;

-- How to select specific table with columns
SELECT * FROM `table_name` WHERE `table_name`.`target_columns` = 1;

-- How to SELECT specific columns in table
SELECT `columns1`,`columns2` FROM `table_name`;

-- How to select specific table with specific columns
SELECT `columns1`,`columns2` FROM `table_name` WHERE `table_name`.`target_columns` = 1;